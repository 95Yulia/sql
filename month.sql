       DATENAME(month, CONCAT(YEAR(GETDATE()),'-01','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-02','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-02','-01')) AS Month
