       DATENAME(month, CONCAT(YEAR(GETDATE()),'-06','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-07','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-07','-01')) AS Month
