       DATENAME(month, CONCAT(YEAR(GETDATE()),'-08','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-09','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-09','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-10','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-10','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-11','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-11','-01')) AS Month
UNION  EOMONTH(CONCAT(YEAR(GETDATE()),'-12','-01')) AS Days, 
       DATENAME(month, CONCAT(YEAR(GETDATE()),'-12','-01')) AS Month